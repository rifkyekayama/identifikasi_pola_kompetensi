    public void setAttributes(ArrayList<String[]> parseData, ArrayList<String> cleanData, int batasMinimum){
        this.parseData = (ArrayList<String[]>)parseData.clone();
        this.cleanData = (ArrayList<String>)cleanData.clone();
        this.batasMinimum = batasMinimum;
        
        frekuensi = new Frekuensi(parseData, cleanData, batasMinimum);
        frekuensi.generateTableTransaction();
        frekuensi.getSumTransactions();
        frekuensi.getMinimumTransactions(batasMinimum);
        frekuensi.getCombination();
        
        associationRuleMining = new AssociationRuleMining(frekuensi.sumOfCombination, frekuensi.tableTransaction);
        associationRuleMining.buildRule();
        rules = (ArrayList<String[]>)associationRuleMining.rules.clone();
        
        buildAssociationTable();
    }

