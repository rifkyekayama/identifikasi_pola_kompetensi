   private void buildAssociationTable(){
       DefaultTableModel tabelModel = new DefaultTableModel(){};    
       tabelModel.addColumn("No");
       tabelModel.addColumn("If antecedent then consequent");
       tabelModel.addColumn("Support");
       tabelModel.addColumn("Confidence");
       
       try{
            for(int i=0;i<rules.size(); i++){
                Object[] x = new Object[4];
                x[0] = (i+1);
                x[1] = rules.get(i)[1];
                x[2] = Math.round(Double.parseDouble(rules.get(i)[3]))+"%";
                x[3] = Math.round(Double.parseDouble(rules.get(i)[4]))+"%";
                tabelModel.addRow(x);
            }
            Table_AR2.setModel(tabelModel);
        }catch(Exception ex){
        }
   }
  
