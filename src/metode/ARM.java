    private void btnproses_arm1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnproses_arm1ActionPerformed
        // TODO add your handling code here:
        int i,j,idx=1;
        
        listsHasil = (ArrayList<Object[]>)associationRuleMining.getMinimumSupportAndConfidenceLists(rules, (int)jSlider1.getValue(), (int)jSlider2.getValue()).clone();
        
        DefaultTableModel tabelModel = new DefaultTableModel(){};
        tabelModel.addColumn("No");
        tabelModel.addColumn("If antecedent then consequent");
        tabelModel.addColumn("Support");
        tabelModel.addColumn("Confidence");
        tabelModel.addColumn("Support x Confidence");
        
        for(i=0;i<listsHasil.size();i++){
            Object[] data = new Object[5];
            data[0] = idx;
            data[1] = listsHasil.get(i)[1];
            data[2] = listsHasil.get(i)[2]+"%";
            data[3] = listsHasil.get(i)[3]+"%";
            data[4] = listsHasil.get(i)[4];
            tabelModel.addRow(data);
            idx++;
        }
        
        Table_AR1.setModel(tabelModel);
        
        kesimpulan.setAttribute(listsHasil);
        kesimpulan.buildConclusion();
    }//GEN-LAST:event_btnproses_arm1ActionPerformed

